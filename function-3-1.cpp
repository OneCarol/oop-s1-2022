#include <iostream>
using namespace std;

int count_numbers_in_range(int vals[], int length, int bottom, int top){
    int arr=0;
    int len;
    int bot;
    int topone;

    for(int i=0;i<len;i++){
        if(vals[i] <= topone && vals[i] >= bot){

            arr == vals[i];
            arr++;

            cout << arr << ",";
        }

    }

    cout << endl;

    return arr;
}
